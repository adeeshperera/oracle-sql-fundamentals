
-- =====================================================
-- BASIC SELECT STATEMENT - Viewing All Data
-- =====================================================
-- Demonstrates how to retrieve all columns and rows from tables
-- The asterisk (*) represents all columns in the table

-- View all employee records
SELECT * 
FROM employee;

-- View all department records
SELECT * 
FROM department;

-- View all customer records
SELECT * 
FROM customer;

-- View all product records
SELECT * 
FROM product;

-- View all customer order records
SELECT * 
FROM customer_order;

